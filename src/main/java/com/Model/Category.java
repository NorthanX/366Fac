package com.Model;

public class Category {
    private int ID;
    private int BlogID;
    private int TagID;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getBlogID() {
        return BlogID;
    }

    public void setBlogID(int blogID) {
        this.BlogID = blogID;
    }

    public int getTagID() {
        return TagID;
    }

    public void setTagID(int tagID) {
        this.TagID = tagID;
    }
}
