package com.Model;

public class Tag {
    private int TagID;
    private String TagName;
    private long TagNum;

    public int getTagID() {
        return TagID;
    }

    public void setTagID(int tagID) {
        TagID = tagID;
    }

    public String getTagName() {
        return TagName;
    }

    public void setTagName(String tagName) {
        TagName = tagName;
    }

    public long getTagNum() {
        return TagNum;
    }

    public void setTagNum(long tagNum) {
        TagNum = tagNum;
    }
}
